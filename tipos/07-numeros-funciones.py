import math  # importa la libreria matematica

print(round(1.3))  # redondea a numero mas entero más cercano
print(round(1.7))  # redondea a numero mas entero más cercano
print(abs(-77))  # valor absoluto, siempre devuelve número positivo

print(math.ceil(1.1))  # redondea a numero mas cercano
print(math.floor(1.9999))  # redondea a numero mas cercano abajo
print(math.isnan(23))  # verifica si es un numero responde booleano
print(math.pow(10, 3))  # primero número elevado a 3
print(math.sqrt(100))  # raiz cuadrada
